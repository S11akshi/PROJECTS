        "import streamlit as st\n",
        "import pandas as pd\n",
        "import numpy as np\n",
        "from sklearn.preprocessing import MinMaxScaler\n",
        "\n",
        "# -----------------------------------\n",
        "# Page Configuration\n",
        "# -----------------------------------\n",
        "st.set_page_config(\n",
        "    page_title=\"Shipping Analytics Dashboard\",\n",
        "    layout=\"wide\"\n",
        ")\n",
        "\n",
        "st.title(\"üì¶ Shipping Performance Analytics Dashboard\")\n",
        "\n",
        "# -----------------------------------\n",
        "# Load Cleaned Data\n",
        "# -----------------------------------\n",
        "df = pd.read_csv('/content/cleaned_data.csv')\n",
        "\n",
        "# Convert date column (if already datetime this won't harm)\n",
        "df['Order Date'] = pd.to_datetime(df['Order Date'])\n",
        "\n",
        "# -----------------------------------\n",
        "# Sidebar Filters (User Capabilities)\n",
        "# -----------------------------------\n",
        "st.sidebar.header(\"üîé Filters\")\n",
        "\n",
        "# Date Range Filter\n",
        "date_range = st.sidebar.date_input(\n",
        "    \"Select Date Range\",\n",
        "    [df['Order Date'].min(), df['Order Date'].max()]\n",
        ")\n",
        "\n",
        "# Region Filter\n",
        "region_filter = st.sidebar.multiselect(\n",
        "    \"Select Region\",\n",
        "    options=df['Region'].unique(),\n",
        "    default=df['Region'].unique()\n",
        ")\n",
        "\n",
        "# Ship Mode Filter\n",
        "ship_mode_filter = st.sidebar.multiselect(\n",
        "    \"Select Ship Mode\",\n",
        "    options=df['Ship Mode'].unique(),\n",
        "    default=df['Ship Mode'].unique()\n",
        ")\n",
        "\n",
        "# Lead Time Threshold\n",
        "lead_threshold = st.sidebar.slider(\n",
        "    \"Minimum Lead Time (Days)\",\n",
        "    min_value=int(df['Shipping_lead_time'].min()),\n",
        "    max_value=int(df['Shipping_lead_time'].max()),\n",
        "    value=int(df['Shipping_lead_time'].min())\n",
        ")\n",
        "# Route Level\n",
        "route_type = st.sidebar.radio(\n",
        " \"Select route Level\",\n",
        "   [\"Region Route\",\"State Route\"]\n",
        ")\n",
        "if route_type == \"Region Route\":\n",
        "  route_column = \"Region_Route\"\n",
        "else:\n",
        "  route_column = \"State_Route\"\n",
        "# -----------------------------------\n",
        "# Apply Filters\n",
        "# -----------------------------------\n",
        "filtered_df = df[\n",
        "    (df['Order Date'].between(pd.to_datetime(date_range[0]), pd.to_datetime(date_range[1]))) &\n",
        "    (df['Region'].isin(region_filter)) &\n",
        "    (df['Ship Mode'].isin(ship_mode_filter)) &\n",
        "    (df['Shipping_lead_time'] >= lead_threshold)\n",
        "]\n",
        "\n",
        "# -----------------------------------\n",
        "# KPI Section\n",
        "# -----------------------------------\n",
        "st.subheader(\"üìä Key Performance Indicators\")\n",
        "\n",
        "col1, col2, col3 = st.columns(3)\n",
        "\n",
        "col1.metric(\"Total Orders\", len(filtered_df))\n",
        "col2.metric(\"Average Lead Time\", round(filtered_df['Shipping_lead_time'].mean(), 2))\n",
        "col3.metric(\"Max Lead Time\", round(filtered_df['Shipping_lead_time'].max(), 2))\n",
        "\n",
        "st.markdown(\"---\")\n",
        "\n",
        "# -----------------------------------\n",
        "# Route Efficiency Overview\n",
        "# -----------------------------------\n",
        "st.header(\"üö¶ Route Efficiency Overview\")\n",
        "\n",
        "scaler = MinMaxScaler()\n",
        "\n",
        "route_summary = (\n",
        "    filtered_df\n",
        "    .groupby(route_column)\n",
        "    .agg(\n",
        "        avg_lead_time=('Shipping_lead_time', 'mean')\n",
        "    )\n",
        ")\n",
        "\n",
        "route_summary['efficiency_score'] = 1 - scaler.fit_transform(\n",
        "    route_summary[['avg_lead_time']]\n",
        ")\n",
        "\n",
        "route_summary = route_summary.sort_values(\n",
        "    by='efficiency_score', ascending=False\n",
        ")\n",
        "\n",
        "col1, col2 = st.columns(2)\n",
        "\n",
        "with col1:\n",
        "    st.subheader(\"Average Lead Time by Route\")\n",
        "    st.bar_chart(route_summary['avg_lead_time'])\n",
        "\n",
        "with col2:\n",
        "    st.subheader(\"Route Performance Leaderboard\")\n",
        "    st.dataframe(route_summary)\n",
